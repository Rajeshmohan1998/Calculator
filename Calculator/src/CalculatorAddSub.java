
public class CalculatorAddSub {
	
	//Addition method
	public void add(double num1, double num2) {
		double addResult = num1 + num2;
		System.out.print("\nAddition of two values are: " + addResult);
	}
	
	//subtraction method
	public void sub(double num1, double num2) {
		double subResult = num1 - num2;
		System.out.print("\nSubtraction of two values are: " + subResult);
	}

}


public class CalculatorProDiv {
	
	//Product method
	public void product(double num1, double num2) {
		double proResult = num1 * num2;
		System.out.print("\nProduct of Two values are: " + proResult);
	}
	
	//Division method
	public void divide(double num1, double num2) {
		double divResult = num1 / num2;
		System.out.print("\nDivision of Two values are: " + divResult);
	}

}
